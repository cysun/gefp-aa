package csula.edu.gefp.model;

public class Runway {

    Long id;

    String name;

    public Runway() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
